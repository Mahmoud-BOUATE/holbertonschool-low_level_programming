#include "3-calc.h"
#include <stdio.h>

/**
* op_add - function that add 2 integers
* @a: operator 1
* @b: operator 2
* Return: return the addition of 2 integers
*/
int op_add(int a, int b)
{
return (a + b);
}


/**
* op_sub - function that sub 2 integers
* @a: operator 1
* @b: operator 2
* Return: return the sub of 2 integers
*/
int op_sub(int a, int b)
{
return (a - b);
}


/**
* op_mul - function that mul 2 integers
* @a: operator 1
* @b: operator 2
* Return: return the mul of 2 integers
*/
int op_mul(int a, int b)
{
return (a * b);
}


/**
* op_div - function that div 2 integers
* @a: operator 1
* @b: operator 2
* Return: return the div of 2 integers
*/
int op_div(int a, int b)
{
return (a / b);
}


/**
* op_mod - function that mod 2 integers
* @a: operator 1
* @b: operator 2
* Return: return the modulo of 2 integers
*/
int op_mod(int a, int b)
{
return (a % b);
}

